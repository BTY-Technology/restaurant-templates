// Helper functions
export const getFeaturedItems = (): MenuItem[] => {
  return menuItems.filter(item => item.featured);
};

export const getItemsByCategory = (category: string): MenuItem[] => {
  return menuItems.filter(item => item.category === category);
};

export const getItemById = (id: string): MenuItem | undefined => {
  return menuItems.find(item => item.id === id);
};

export const getRelatedItems = (itemId: string): MenuItem[] => {
  const item = getItemById(itemId);
  if (!item || !item.relatedItems) return [];
  return item.relatedItems
    .map(id => getItemById(id))
    .filter(Boolean) as MenuItem[];
};

export const searchItems = (query: string): MenuItem[] => {
  const lowerQuery = query.toLowerCase();
  return menuItems.filter(
    item =>
      item.name.toLowerCase().includes(lowerQuery) ||
      item.description.toLowerCase().includes(lowerQuery) ||
      item.ingredients.some(ing => ing.toLowerCase().includes(lowerQuery))
  );
};
