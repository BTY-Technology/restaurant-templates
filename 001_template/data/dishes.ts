export const getFeaturedDishes = (): Dish[] => {
  return dishes.filter(dish => dish.featured);
};

export const getDishesByCategory = (category: string): Dish[] => {
  return dishes.filter(dish => dish.category === category);
};

export const getDishById = (id: string): Dish | undefined => {
  return dishes.find(dish => dish.id === id);
};

export const getPopularDishes = (): Dish[] => {
  return dishes.filter(dish => dish.popular).slice(0, 8);
};

export const getRelatedDishes = (dishId: string): Dish[] => {
  const dish = getDishById(dishId);
  if (!dish || !dish.relatedDishes) return [];

  return dish.relatedDishes
    .map(id => getDishById(id))
    .filter((d): d is Dish => d !== undefined);
};
